    <h3 class="tit_1">打破 — 三分法构图</h3>
    <p class="txt">在近年来看到的一些最有趣的例子，就是摄影机的构图违反了“三分法”。一个是《黑客军团》中的负空间构图。另一个例子则是韦斯·安德森和DP罗伯特·约曼的作品，这些作品经常在把角色设置在画面居中构图，在变形镜头的帮助下，造成某种俏皮的样子。</p>
