    <h4 class="tit_2">《飓风》</h4>
    <p class="txt">来自《神枪手之死》的图片中，在刺杀杰斯·詹姆斯的两个镜头里，懦夫罗伯特·福特在画面中，我们看到剪影被用来增加“震慑”价值的时刻。同样的情况是真实的逃犯，站在迎面而来的火车头灯前，相信火车不会跑过来，要不这片就这样结束了。现场将紧张局势推向了突破点，但如果我们看到更多细节，就会让自己分心，不会那么紧张了。当在电影中制作特别紧张的场景时，请考虑使用剪影来克服张力。考虑下你想要观众如何感受，并考虑剪影如何将这种感觉植入观众心目中。</p>
