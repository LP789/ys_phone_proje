    <p class="txt">当在《锅盖头》中杰米·福克斯饰演的Sykes从他的水壶里喝了一口水时，使用剪影有助于我们感受到水的滋味，帮助大家了解他是有多么的热，以及环境下的压力。电影结合了战士与地球之间，士兵与周围的火焰之间，士兵与他所饮用的水之间的关系。以这样一种方式，可以看到每一个关系都发着光，让我们了解了这个看似简单的时刻的重要性。在简练的、具体化的视觉画像中使用剪影会定义角色和情境。</p>
