    <p class="txt">很多时候我们愿意去总结经验和相信规则，试图找到一个定式去应对迎面而来的大部分问题，这样是会得到稳妥得当的结果，但如果在电影行业当中，往往能成为经典电影与称为大师的人却是因为打破了规则。</p>
